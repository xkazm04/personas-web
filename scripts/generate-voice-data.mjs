/**
 * Generate src/lib/review-voice-data.ts — ordered (best-first) Web Speech voice
 * names per supported locale, for the review voice-announcer's voice selection.
 *
 * Source: HadrienGardeur/web-speech-recommended-voices (Readium Speech project),
 * read from a local clone's git history (the JSON files were later migrated to
 * readium/speech, but remain in this repo's history). Each locale's `voices`
 * array is already ordered by quality; we flatten name + altNames per voice,
 * preserving order, so the runtime picks the highest-quality *available* voice.
 *
 *   node scripts/generate-voice-data.mjs [repoPath] [gitRef]
 *
 * Defaults target the user's clone at the pre-migration commit.
 */
import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const REPO = process.argv[2] || "C:/Users/kazda/kiro/web-speech-recommended-voices";
// Use ~1 (not ^): Node's execSync runs via cmd.exe on Windows, where ^ is the
// escape character and would be stripped from the git revision.
const REF = process.argv[3] || "741a0d3~1";

// app locale -> source json basename (zh uses Mandarin "cmn").
const MAP = {
  en: "en", ar: "ar", bn: "bn", cs: "cs", de: "de", es: "es", fr: "fr",
  hi: "hi", id: "id", ja: "ja", ko: "ko", ru: "ru", vi: "vi", zh: "cmn",
};

const out = {};
for (const [loc, file] of Object.entries(MAP)) {
  const raw = execSync(`git -C "${REPO}" show ${REF}:json/${file}.json`, {
    encoding: "utf8",
    maxBuffer: 16 * 1024 * 1024,
  });
  const data = JSON.parse(raw);
  const names = [];
  const seen = new Set();
  for (const v of data.voices ?? []) {
    for (const n of [v.name, ...(v.altNames ?? [])]) {
      if (n && !seen.has(n)) {
        seen.add(n);
        names.push(n);
      }
    }
  }
  out[loc] = names;
}

const header = `// AUTO-GENERATED by scripts/generate-voice-data.mjs — do not edit by hand.
// Source: HadrienGardeur/web-speech-recommended-voices @ ${REF} (Readium Speech),
// ordered best-first per language (name + altNames flattened). zh = Mandarin (cmn).
//
// Used by src/lib/review-voice.ts to pick the highest-quality *available* Web
// Speech voice for each supported locale, falling back to a locale match then
// the browser default.

`;
const body = `export const RECOMMENDED_VOICES: Record<string, readonly string[]> = ${JSON.stringify(out, null, 2)};\n`;
writeFileSync("src/lib/review-voice-data.ts", header + body);

console.log("voices per locale:");
for (const [k, v] of Object.entries(out)) console.log(`  ${k}: ${v.length}`);
console.log("output bytes:", (header + body).length);
